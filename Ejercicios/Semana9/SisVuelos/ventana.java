    public void modificarArchivo(String ruta, String buscado,String nuevo){
        List<String> lineas =new ArrayList<>(); 
        try (BufferedReader leer = new BufferedReader(new FileReader(ruta))) {
            String linea;
            while ((linea = leer.readLine()) != null) {
                String[] partes = linea.split(",");
                if (partes[1].equals(buscado)) {
                    lineas.add(nuevo); 
                } else {
                    lineas.add(linea);       
                }
            }
        } catch (IOException e) {
            System.out.println("Error " + e.getMessage());
            return;
        }

        try (BufferedWriter escribir = new BufferedWriter(new FileWriter(ruta))) {
            for (int i = 0; i < lineas.size(); i++) {
            String lin = lineas.get(i);
            escribir.write(lin);
            escribir.newLine();
        }            
        } catch (IOException e) {
            System.out.println("Error " + e.getMessage());
        }
    }
  
    public void escribirArchivo(String ruta,String contenido){
        try (FileWriter escritor = new FileWriter(ruta, true)) {
            escritor.write(contenido);
            escritor.write("\n");
        } catch (IOException e) {
            System.out.println("Ocurrió un error al escribir en el archivo: " + e.getMessage());
        }
    }
    public String DevolverLinea(String ruta,String contenido, int Posicion){
        String linea;
        try (BufferedReader lector = new BufferedReader(new FileReader(ruta))) {            
            while ((linea = lector.readLine()) != null) {
                String[] arreglo = linea.split(",");
                if (arreglo[Posicion].equals(contenido)) {
                    break;                                        
                }
            }
        } catch (IOException e) {
            System.out.println("Error al leer el archivo: " + e.getMessage());            
            linea="";
        }
        return linea;
        
    }
    public boolean Comparar(String ruta,String contenido,int Posicion){
        boolean existe = false;        

        try (BufferedReader lector = new BufferedReader(new FileReader(ruta))) {
            String linea;
            while ((linea = lector.readLine()) != null) {
                String[] arreglo = linea.split(",");
                if (arreglo[Posicion].equals(contenido)) {
                    existe = true;                    
                    break;
                }
            }
        } catch (IOException e) {
            System.out.println("Error al leer el archivo: " + e.getMessage());
            
        }
        return existe;
    }
    public void desplegarArchivo(File f){
        Texto.setText("");
        FileReader archivo;
	BufferedReader filtrado;
	try
        {
            archivo = new FileReader(f);
            filtrado = new BufferedReader(archivo);
            String linea = "";
            int cont = 0;
            while ((linea = filtrado.readLine()) != null){
                if (!linea.equals("null")){
                    Texto.append(linea +"\n");
                }
		cont++;
		if (cont == 999){
			break;
                }
            }
            archivo.close();
            filtrado.close();	
        }
        catch(FileNotFoundException e)
        {
                Texto.append(e.getMessage());
        }
        catch(IOException e)
        {
                Texto.append(e.getMessage());
        }
}
    public void guardarArchivo(File q){
        FileWriter archivo;
        BufferedWriter filtrado;
        String contenido = "";
        try
        {
            archivo = new FileWriter(q);
            filtrado = new BufferedWriter(archivo);
            contenido = Texto.getText();
            filtrado.write(contenido,0,contenido.length());
            filtrado.flush();
            filtrado.close();
            archivo.close();
        }
        catch(FileNotFoundException e)
        {
            Texto.append(e.getMessage());
        }
        catch(IOException e)
        {
            Texto.append(e.getMessage());
        }
        Texto.setText("");
}
    
