    private void buscarBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarBActionPerformed
        // TODO add your handling code here:       
        boolean encontrado=false;
        
        for(int i=0;i<an.getVuelo().size();i++){        
            if(an.getVuelo().get(i).getDestino().equals(buscarT.getText())){
               encontrado=true;
               TextoEncontrado.setText(an.getVuelo().get(i).getDatos());
               CambiarPanel(BusquedaE);               
               break;
            }            
        }
        if(!encontrado){
           for(int i=0;i<ai.getVuelo().size();i++){        
            if(ai.getVuelo().get(i).getDestino().equals(buscarT.getText())){          
               TextoEncontrado.setText(ai.getVuelo().get(i).getDatos());
               CambiarPanel(BusquedaE);
               break;
            }
            else
               CambiarPanel(BusquedaF);
        }           
        }
    }//GEN-LAST:event_buscarBActionPerformed

