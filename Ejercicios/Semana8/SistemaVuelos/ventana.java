    private void ReservarConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ReservarConfirmarActionPerformed
        // TODO add your handling code here:
        bolea=true;
        boolean encontrado=false;        
        Pasajero pas=new Pasajero(Nombre.getText(),ID.getText(),Nacionalidad.getText(),Genero.getSelectedItem().toString());
        for(int i=0;i<an.getVuelo().size();i++){        
            if(an.getVuelo().get(i).getDestino().equals(buscarT.getText())){
               encontrado=true;               
               pas.ComprarBoleto(an.getVuelo().get(i).getTipoVuelo(), an.getVuelo().get(i).getPrecio(),  an.getVuelo().get(i).getFechaSalida(), an.getVuelo().get(i).getFechaRegreso(),an.getVuelo().get(i).getCodigoVuelo(),an.getVuelo().get(i).getDestino(),an.getVuelo().get(i).getOrigen(),2);
               BoletoVer.setText(pas.VerVuelo());
               CambiarPanel(Boleto);           
               buscarT.setText("");
               break;
            }            
        }
        if(!encontrado){
           for(int i=0;i<ai.getVuelo().size();i++){        
            if(ai.getVuelo().get(i).getDestino().equals(buscarT.getText())){  
               pas.ComprarBoleto(ai.getVuelo().get(i).getTipoVuelo(), ai.getVuelo().get(i).getPrecio(),  ai.getVuelo().get(i).getFechaSalida(), ai.getVuelo().get(i).getFechaRegreso(),ai.getVuelo().get(i).getCodigoVuelo(),ai.getVuelo().get(i).getDestino(),ai.getVuelo().get(i).getOrigen(),2);
               BoletoVer.setText(pas.VerVuelo());
               CambiarPanel(Boleto);
               buscarT.setText("");
               break;
            }         
        }         
     }
     CambiarPanel(Boleto);
    }//GEN-LAST:event_ReservarConfirmarActionPerformed

