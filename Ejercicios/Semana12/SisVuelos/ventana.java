   public String RecuperarDatos(String buscado, String tabla,String opcion){
       String datos="";
       try{
            Connection conexion = DriverManager.getConnection("jdbc:ucanaccess://src/base/bdAerolinea.accdb");
            Statement estatuto = conexion.createStatement();
            String sql="SELECT * FROM " + tabla;          
            ResultSet rs = estatuto.executeQuery(sql);
            while(rs.next()){
		String id = "";
                switch(tabla){
                    case "Pasajero":
                        id = rs.getString("Cedula");                                            
                        if(id.equals(buscado)){
                            String nombre=rs.getString("Nombre");
                            String genero=rs.getString("Genero");
                            String nacionalidad=rs.getString("Nacionalidad"); 
                            datos=id+"&"+nombre+"&"+genero+"&"+nacionalidad;
                            break;
                        }             
                        break;
                    case "Vuelo":
                        if(opcion.equals("1")){
                            id = rs.getString("idVuelo");
                            if(id.equals(buscado)){
                                String Precio=Float.toString(rs.getFloat("Precio"));
                                String Tipo=rs.getString("Tipo");
                                String Destino=rs.getString("Destino");
                                String Origen=rs.getString("Origen");
                                String FechaSalida=rs.getString("FechaSalida");
                                String FechaLlegada=rs.getString("FechaLlegada");
                                datos=id+"&"+Precio+"&"+Tipo+"&"+Destino+"&"+Origen+"&"+FechaSalida+"&"+FechaLlegada;
                            break;
                            }
                        }                            
                        else{
                            id=rs.getString("Destino");
                            if(id.equals(buscado)){
                                id=rs.getString("idVuelo");
                                String Precio=Float.toString(rs.getFloat("Precio"));
                                String Tipo=rs.getString("Tipo");
                                String Destino=rs.getString("Destino");
                                String Origen=rs.getString("Origen");
                                String FechaSalida=rs.getString("FechaSalida");
                                String FechaLlegada=rs.getString("FechaLlegada");
                                datos=id+"&"+Precio+"&"+Tipo+"&"+Destino+"&"+Origen+"&"+FechaSalida+"&"+FechaLlegada;
                            break;
                            }
                        }
                            
                        
                        break;
                    case "Boleto":
                        id = rs.getString("IdBoleto");
                        if(id.equals(buscado)){
                            String Asiento=rs.getString("Asiento");
                            String IdVuelo=rs.getString("IdVuelo");
                            String cedula = rs.getString("Cedula");
                            String nombre=rs.getString("Nombre");
                            String genero=rs.getString("Genero");
                            String nacionalidad=rs.getString("Nacionalidad"); 
                            String Precio=Float.toString(rs.getFloat("Precio"));
                            String Tipo=rs.getString("Tipo");
                            String Destino=rs.getString("Destino");
                            String Origen=rs.getString("Origen");
                            String FechaSalida=rs.getString("FechaSalida");
                            String FechaLlegada=rs.getString("FechaLlegada");
                            datos=id+"&"+Asiento+"&"+cedula+"&"+nombre+"&"+genero+"&"+nacionalidad+"&"+IdVuelo+"&"+Precio+"&"+Tipo+"&"+Destino+"&"+Origen+"&"+FechaSalida+"&"+FechaLlegada;
                            break;
                        }                        
                        break;               
                }
              
               
            }
            estatuto.close();
            conexion.close();
	}
	catch(SQLException e)
	{
            System.out.println(e.getMessage());
	}
       return datos;
   }
     
   public boolean Existe(String comprobar,String tabla,String opcion){
       boolean existe=false;
       try{
            Connection conexion = DriverManager.getConnection("jdbc:ucanaccess://src/base/bdAerolinea.accdb");
            Statement estatuto = conexion.createStatement();
            String sql="SELECT * FROM " + tabla;
            String id="";
            ResultSet rs = estatuto.executeQuery(sql);
            while(rs.next()){
                if(tabla.equals("Pasajero")){
                    id = rs.getString("cedula");
                }
                else if(tabla.equals("Vuelo")){
                    if(opcion.equals("1"))
                        id = rs.getString("IdVuelo");
                    else
                        id=rs.getString("Destino");
                }
                else{
                    id = rs.getString("IdBoleto");
                }
		if (comprobar.equals(id)){
                    existe=true;
                    break;
                }
            }
            estatuto.close();
            conexion.close();
	}
	catch(SQLException e)
	{
            System.out.println(e.getMessage());
	}
       return existe;
   }
